var proto_downloaded = {};

function download(proto, url, cb) {
    //this protocol was successfully downloaded
    if( proto_downloaded[proto] === true ){
        if( cb ) cb();
        return;
    }
    
    
    var file = fs.createWriteStream( DIR + "/" + proto + ".txt" );

    var downloader = http;
    if( url.indexOf( "https") === 0 )
        downloader = https;
    
    console.log( proto + " downloading " + url);
    
    downloader.get(url, function (response) {
        response.pipe(file);
        file.on('finish', function() {
            file.close( function(){
                console.log( "       ==>   finished " + url );
                proto_downloaded[ proto ] = true;
                if( cb ) cb();
            });
        });
    }).on('error', function(err) { // Handle errors
        console.log("error when downloading " + url );
        console.log( err.stack );
        if( cb ) cb( err );
    });
}


